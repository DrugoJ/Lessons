public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        Byte b = 5;             // Value is displayed
        Short s = 5;            // Value is displayed
        Long l = 5l;            // Value is displayed
        Integer i = 5;          // Value is displayed
        Double d = 5.0;         // Value is displayed
        Boolean bl = true;      // Value is displayed
        Float f = 5f;           // Value is displayed
        Character c = 5;        // Number value is displayed, but String value isn't
        String str = "Hello";   // Value is displayed
        //Math - Class with math methods
        System.out.println(c);  // Value is displayed
    }
    
}
